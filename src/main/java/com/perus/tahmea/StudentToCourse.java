package com.perus.tahmea;

public class StudentToCourse {
    private String Students;
    private String Courses;


    public StudentToCourse(String students, String courses) {
        Students = students;
        Courses = courses;

    }

    public String getStudents() {
        return Students;
    }

    public String getCourses() {
        return Courses;
    }

}
